public class Prioridade {
    private int id;
    private String descricao;
    private String diasParaConclusao;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getDiasParaConclusao() {
        return diasParaConclusao;
    }

    public void setDiasParaConclusao(String diasParaConclusao) {
        this.diasParaConclusao = diasParaConclusao;
    }
    
}
